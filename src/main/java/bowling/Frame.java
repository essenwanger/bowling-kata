package bowling;

public class Frame {

	private int rollOne;
	private int rollTwo;
	
	public Frame() {
		this.rollOne = 0;
		this.rollTwo = 0;
	}

	public int getRollOne() {
		return rollOne;
	}

	public void setRollOne(int rollOne) {
		this.rollOne = rollOne;
	}

	public int getRollTwo() {
		return rollTwo;
	}

	public void setRollTwo(int rollTwo) {
		this.rollTwo = rollTwo;
	}
	
}
